buah = ["apel", "anggur", "mangga", "jeruk"]

# Mencetak buah pada indeks ke-2 (yaitu "mangga")
print("Isi buah indeks ke-2 adalah: {}".format(buah[2]))

# Menghitung dan mencetak jumlah buah
print("Semua buah ada {} macam".format(len(buah)))

# Mencetak semua buah dalam daftar
for x in buah:
    print(x)
